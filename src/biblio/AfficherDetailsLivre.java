    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // R√©cup√©rer l'ISBN saisi par l'utilisateur
    String isbn = jTextField2.getText();
    
    // Rechercher le livre dans la biblioth√®que en fonction de l'ISBN
    Livre livreTrouve = Bibliotheque.rechercherParISBN(isbn);
    
    // V√©rifier si le livre a √©t√© trouv√©
    if (livreTrouve != null) {
        // V√©rifier le type de livre et afficher les d√©tails en cons√©quence
        if (livreTrouve instanceof Roman) {
            Roman roman = (Roman) livreTrouve;
            JOptionPane.showMessageDialog(this, roman.toString(), "D√©tails du Roman", JOptionPane.INFORMATION_MESSAGE);
        } else if (livreTrouve instanceof Essai) {
            Essai essai = (Essai) livreTrouve;
            JOptionPane.showMessageDialog(this, essai.toString(), "D√©tails de l'Essai", JOptionPane.INFORMATION_MESSAGE);
        } else if (livreTrouve instanceof LivreAudio) {
            LivreAudio livreAudio = (LivreAudio) livreTrouve;
            JOptionPane.showMessageDialog(this, livreAudio.toString(), "D√©tails du Livre Audio", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Type de livre non reconnu.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // Afficher un message d'erreur si le livre n'a pas √©t√© trouv√©
        JOptionPane.showMessageDialog(this, "Aucun livre trouv√© avec cet ISBN.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1MouseClicked

