    public class user{
    private String prenom;
    private String nom;
    private String isbn;
    public user(String prenom, String nom, String isbn) {
        this.prenom = prenom;
        this.nom = nom;
        this.isbn = isbn;
    }
    }
    
    public ArrayList ListUsers(){
        ArrayList<user> list = new ArrayList();
        user u1 = new user("Komodo", "Benalgodev", "1");
        user u2 = new user("Diara", "Lopes", "2");
        list.add(u1);
        list.add(u2);
        
        return list;
    }
    
    public void remplirTableau(){
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        ArrayList<user> list = ListUsers();
        Object rowData[] = new Object[3];
        for(int i=0; i<list.size(); i++){
            rowData[0] = list.get(i).isbn;
            rowData[1] = list.get(i).prenom;
            rowData[2] = list.get(i).nom;
            model.addRow(rowData);
        }
    }

